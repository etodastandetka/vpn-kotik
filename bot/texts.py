def support_html(support_username: str, support_info_url: str) -> str:
    lines = [
        "💬 <b>Поддержка</b>",
        "",
        "Если что-то не получается — <b>опишите одним сообщением</b>:",
        "• что хотели сделать (оплатить / подключить / промокод);",
        "• на каком шаге остановились;",
        "• скрин ошибки (если есть).",
        "",
    ]
    un = (support_username or "").strip().lstrip("@")
    if un:
        lines.append(f"Написать: <a href=\"https://t.me/{un}\">@{un}</a>")
    else:
        lines.append("Контакт поддержки скоро будет указан здесь. Пока опишите проблему ответом на это сообщение в чате с ботом (если включена пересылка администратору).")
    if support_info_url.strip():
        lines.extend(["", f"📎 <a href=\"{support_info_url.strip()}\">Доп. информация / сайт</a>"])
    lines.append("")
    lines.append("<i>Ответ обычно в течение рабочего времени — зависит от загрузки.</i>")
    return "\n".join(lines)

